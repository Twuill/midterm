public class AddIntegers
{
    public static void main(String[] args)
    {
        int first = 50;
        int second = 150;
        int third = 35;

        System.out.println("Enter two numbers: " + first + " " + second + " " + third);
        int sum = first + second + third;

        System.out.println("The sum is: " + sum);
    }
}
